# 1. Should not start with a number
# 2. Should not contain special characters except underscore (_)
# 3. Should not use reserved keywords like 'if', 'for', etc.
# Incorrect variable names:
# first-name = "NAME"    # Incorrect due to the hyphen
# 0001name = "NAME"      # Incorrect as it starts with a number
# 1name = "NAME"         # Incorrect as it starts with a number
# if = "NAME"            # Incorrect as it uses a reserved keyword 'if'
# for = "NAME"           # Incorrect as it uses a reserved keyword 'for'
